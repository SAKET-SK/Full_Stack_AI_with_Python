# We are to iterate through a list for this scenario.
# The list contains the names of people invited to a party. The goal is to print a message for each person in the list.

# Ex : Greetings, [name]. You are invited to the party! Lets Rock and Roll!

invitees = ["Amol", "Rohan", "Sonal", "Soumya", "Sagar"]   # list

for name in invitees:
    print(f"Greetings, {name}. You are invited to my birthday party! Let's Rock and Roll!")
